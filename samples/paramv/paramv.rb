PROCS['paramv'] = proc do
  x = MathGL::MglData.new(20,20,20)
  y = MathGL::MglData.new(20,20,20)
  z = MathGL::MglData.new(20,20,20)
  ex = MathGL::MglData.new(20,20,20)
  ey = MathGL::MglData.new(20,20,20)
  ez = MathGL::MglData.new(20,20,20)
  fill(x,"(x+2)/3*sin(pi*y/2)")
  fill(y,"(x+2)/3*cos(pi*y/2)")
  fill(z,"x+z")
  fill(ex,"x")
  fill(ey,"x^2+y")
  fill(ez,"y^2+z")
  x1 = MathGL::MglData.new(20,20)
  y1 = MathGL::MglData.new(20,20)
  e1 = MathGL::MglData.new(20,20)
  e2 = MathGL::MglData.new(20,20)
  fill(x1,"(x+2)/3*sin(pi*y/2)")
  fill(y1,"(x+2)/3*cos(pi*y/2)")
  fill(e1,"x")
  fill(e2,"x^2+y")
  sub_plot(3,3,0)
  rotate(40,60)
  box()
  vect(x1,y1,e1,e2)
  sub_plot(3,3,1)
  rotate(40,60)
  box()
  flow(x1,y1,e1,e2)
  sub_plot(3,3,2)
  rotate(40,60)
  box()
  pipe(x1,y1,e1,e2)
  sub_plot(3,3,3)
  rotate(40,60)
  box()
  dew(x1,y1,e1,e2)
  sub_plot(3,3,4)
  rotate(40,60)
  box()
  vect(x,y,z,ex,ey,ez)
  sub_plot(3,3,5)
  rotate(40,60)
  box()
  vect3(x,y,z,ex,ey,ez)
  vect3(x,y,z,ex,ey,ez,"x")
  vect3(x,y,z,ex,ey,ez,"z")
  grid3(x,y,z,z,"{r9}")
  grid3(x,y,z,z,"{r9}x")
  grid3(x,y,z,z,"{r9}z")
  sub_plot(3,3,6)
  rotate(40,60)
  box()
  flow(x,y,z,ex,ey,ez)
  sub_plot(3,3,7)
  rotate(40,60)
  box()
  pipe(x,y,z,ex,ey,ez)
end
