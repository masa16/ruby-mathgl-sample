PROCS['inplot'] = proc do
  sub_plot(3,2,0)
  title("StickPlot")
  stick_plot(3, 0, 20, 30)
  box("r")
  puts([0],"0","r")
  stick_plot(3, 1, 20, 30)
  box("g")
  puts([0],"1","g")
  stick_plot(3, 2, 20, 30)
  box("b")
  puts([0],"2","b")
  sub_plot(3,2,3,"")
  title("ColumnPlot")
  column_plot(3, 0)
  box("r")
  puts([0],"0","r")
  column_plot(3, 1)
  box("g")
  puts([0],"1","g")
  column_plot(3, 2)
  box("b")
  puts([0],"2","b")
  sub_plot(3,2,4,"")
  title("GridPlot")
  grid_plot(2, 2, 0)
  box("r")
  puts([0],"0","r")
  grid_plot(2, 2, 1)
  box("g")
  puts([0],"1","g")
  grid_plot(2, 2, 2)
  box("b")
  puts([0],"2","b")
  grid_plot(2, 2, 3)
  box("m")
  puts([0],"3","m")
  sub_plot(3,2,5,"")
  title("InPlot")
  box()
  in_plot(0.4, 1, 0.6, 1, true)
  box("r")
  multi_plot(3,2,1, 2, 1,"")
  title("MultiPlot")
  box()
end
