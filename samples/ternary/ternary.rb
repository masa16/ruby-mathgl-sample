PROCS['ternary'] = proc do
  set_ranges(0,1,0,1,0,1)
  x = MathGL::MglData.new(50)
  y = MathGL::MglData.new(50)
  z = MathGL::MglData.new(50)
  rx = MathGL::MglData.new(10)
  ry = MathGL::MglData.new(10)
  a = MathGL::MglData.new(20,30)
  a.modify("30*x*y*(1-x-y)^2*(x+y<1)")
  x.modify("0.25*(1+cos(2*pi*x))")
  y.modify("0.25*(1+sin(2*pi*x))")
  rx.modify("rnd")
  ry.modify("(1-v)*rnd",rx)
  z.modify("x")
  sub_plot(2,2,0)
  title("Ordinary axis 3D")
  rotate(50,60)
  light(true)
  plot(x,y,z,"r2")
  surf(a,"BbcyrR#")
  axis()
  grid()
  box()
  label('x',"B",1)
  label('y',"C",1)
  label('z',"Z",1)
  sub_plot(2,2,1)
  title("Ternary axis (x+y+t=1)")
  ternary(1)
  plot(x,y,"r2")
  plot(rx,ry,"q^ ")
  cont(a)
  line([0.5,0], [0,0.75], "g2")
  axis()
  grid("xyz","B;")
  label('x',"B")
  label('y',"C")
  label('t',"A")
  sub_plot(2,2,2)
  title("Quaternary axis 3D")
  rotate(50,60)
  light(true)
  ternary(2)
  plot(x,y,z,"r2")
  surf(a,"BbcyrR#")
  axis()
  grid()
  box()
  label('t',"A",1)
  label('x',"B",1)
  label('y',"C",1)
  label('z',"D",1)
  sub_plot(2,2,3)
  title("Ternary axis 3D")
  rotate(50,60)
  light(true)
  ternary(1)
  plot(x,y,z,"r2")
  surf(a,"BbcyrR#")
  axis()
  grid()
  box()
  label('t',"A",1)
  label('x',"B",1)
  label('y',"C",1)
  label('z',"Z",1)
end

