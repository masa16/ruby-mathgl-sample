PROCS['param3'] = proc do
  x = MathGL::MglData.new(50,50,50)
  y = MathGL::MglData.new(50,50,50)
  z = MathGL::MglData.new(50,50,50)
  c = MathGL::MglData.new(50,50,50)
  d = MathGL::MglData.new(50,50,50)
  fill(x,"(x+2)/3*sin(pi*y/2)")
  fill(y,"(x+2)/3*cos(pi*y/2)")
  fill(z,"z")
  fill(c,"-2*(x^2+y^2+z^4-z^2)+0.2")
  fill(d,"1-2*tanh(2*(x+y)^2)")
  light(true)
  alpha(true)
  sub_plot(4,3,0)
  rotate(40,60)
  box()
  surf3(x,y,z,c)
  sub_plot(4,3,1)
  rotate(40,60)
  box()
  surf_3c(x,y,z,c,d)
  sub_plot(4,3,2)
  rotate(40,60)
  box()
  surf_3a(x,y,z,c,d)
  sub_plot(4,3,3)
  rotate(40,60)
  box()
  cloud(x,y,z,c)
  sub_plot(4,3,4)
  rotate(40,60)
  box()
  cont3(x,y,z,c)
  cont3(x,y,z,c,"x")
  cont3(x,y,z,c,"z")
  sub_plot(4,3,5)
  rotate(40,60)
  box()
  cont_f3(x,y,z,c)
  cont_f3(x,y,z,c,"x")
  cont_f3(x,y,z,c,"z")
  sub_plot(4,3,6)
  rotate(40,60)
  box()
  dens3(x,y,z,c)
  dens3(x,y,z,c,"x")
  dens3(x,y,z,c,"z")
  sub_plot(4,3,7)
  rotate(40,60)
  box()
  dots(x,y,z,c,"","meshnum 15")
  sub_plot(4,3,8)
  rotate(40,60)
  box()
  dens_x(c,"",0)
  dens_y(c,"",0)
  dens_z(c,"",0)
  sub_plot(4,3,9)
  rotate(40,60)
  box()
  cont_x(c,"",0)
  cont_y(c,"",0)
  cont_z(c,"",0)
  sub_plot(4,3,10)
  rotate(40,60)
  box()
  cont_fx(c,"",0)
  cont_fy(c,"",0)
  cont_fz(c,"",0)
end
